# Vergleichsoperatoren: Das Resultat ist immer True (Wahr) oder False (Falsch)

x = 5
y = 6
print("a = {}, b = {}".format(x, y), end='\n\n')

# a grÃ¶sser als b ---> Augabe: a > b ist False
print('a > b  ist', x > y)

# a kleienr als b --> Augabe: a < b ist True
print('a < b  ist', x < y)

# a ist gleich b ---> Augabe: a == b ist False
print('a == b ist', x == y)

# a ist ungleich b ---> Augabe: a != b ist True
print('a != b ist', x != y)

# a grÃ¶sser gleich als b ---> Augabe: a >= b ist False
print('a >= b ist', x >= y)

# a ist kleiner gleich b ---> Augabe: a <= b ist True
print('a <= b ist', x <= y)
