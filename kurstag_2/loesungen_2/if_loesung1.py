# if Aufgabe 01
# Entscheiden sie ob eine Zahl
# - kleiner als 18
# - gleich 18
# - oder grösser als 18 ist
# und geben dies auf der Konsole aus.

# if Aufgabe - Loesung 01

zahl = 19
if zahl < 18:
    print("Zahl:", zahl, "ist kleiner als 18")
elif zahl == 18:
    print("Zahl:", zahl, "ist gleich 18")
else:
    print("Zahl:", zahl, "ist grösser als 18")