class Taschenrechner:

    @staticmethod
    def add(a, b):
        return a + b

    @staticmethod
    def minus(a, b):
        return a - b

    @staticmethod
    def divide(a, b):
        return a / b

    @staticmethod
    def multiply(a, b):
        return a * b


print(Taschenrechner.add(3, 4))
print(Taschenrechner.minus(3, 4))
print(Taschenrechner.divide(3, 4))
print(Taschenrechner.multiply(3, 4))
